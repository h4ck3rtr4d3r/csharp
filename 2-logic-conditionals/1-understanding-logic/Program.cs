/*
    Introduction to Logic in C#
    ==========================
    Computers are constantly checking the state of something. Is this program running or not? Does this variable exist or not? Is this value equal to that value?

    These yes or no questions demonstrate that a number of binaries - a relationship between two entities - exist. Computers program essentialy function off of binaries: true and false, yes and no, ones and zeros, on and off.

    Distinguish between binaries is the foundation of Boolean logic. Boolean logic is based on the idea that all values are either true or false. Logic is important to compuer science bacause it is an early attempt at translating the human capacity for reason to computers. If a computer can use logic to reason about certain situations, it can use that rationale to make decisions.

    We'll explore how Boolean logic works in C# and show you how you can begin to implement boolean data types and expressions in you own programs.
